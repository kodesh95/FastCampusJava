package fc.java.course1.part2;

public class FourArithmetic {
    public static void main(String[] args) {
        int n1,n2;

        n1=12;
        n2=2;

        System.out.printf("%d + %d = %d%n",n1,n2,n1+n2);
        System.out.printf("%d - %d = %d%n",n1,n2,n1-n2);
        System.out.printf("%d * %d = %d%n",n1,n2,n1*n2);
        System.out.printf("%d / %d = %.1f%n",n1,n2,n1/(float)n2);

    }
}
